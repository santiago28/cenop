$(".montaje").click(function(){
  if ($(this).is(':checked') ) {
    var parent = $(this).parent().parent().parent().parent().attr('id');
    $('.'+parent).prop('checked', false);
    $(this).prop('checked', true);
  }else {
    var parent = $(this).parent().parent().parent().parent().attr('id');
    $('.'+parent).prop('checked', false);
  }
});

